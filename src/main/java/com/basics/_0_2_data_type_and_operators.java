package main.java.com.basics;

public class _0_2_data_type_and_operators {
    public static void main(String[] args) {

        double a = 10;
        double b = 5;

        double c = a + b;
        double d = a - b;
        double e = a * b;
        double f = a / b;

        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);

    }
}
